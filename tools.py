#!/usr/bin/env python


class Calculette:
	def __init__(self):
		pass

	def add(self, x, y):
		return add(x,y)

	def sub(self, x, y):
		return sub(x, y)

	def mul(self, x, y):
		return mul(x, y)

	def div(self, x, y):
		return div(x, y)


def add(x, y):
	return x + y


def add(x, y):
	return x + y


def sub(x, y):
	return x - y


def mul(x, y):
	return x * y


def div(x, y):
	return x / y
